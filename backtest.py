def plot_equity(results: dict[str, pd.DataFrame], strategy_name: str, out_dir: str):
    """Plot equity curves for all tickers."""
    n   = len(results)
    cols = min(3, n)
    rows = (n + cols - 1) // cols
    fig, axes = plt.subplots(rows, cols, figsize=(6 * cols, 4 * rows))
    axes = np.array(axes).flatten() if n > 1 else [axes]

    fig.suptitle(f"Equity Curves - {strategy_name}\n(Rs1 invested at start)",
                 fontsize=14, fontweight="bold", y=1.01)

    for idx, (tkr, res) in enumerate(results.items()):
        ax = axes[idx]
        ax.plot(res.index, res["equity_strat"], color="#1f77b4", lw=1.5, label="Strategy")
        ax.plot(res.index, res["equity_bah"],   color="#ff7f0e", lw=1.2, ls="--", label="Buy & Hold")
        ax.set_title(tkr, fontsize=10, fontweight="bold")
        ax.set_ylabel("Equity (Rs)")
        ax.legend(fontsize=7)
        ax.grid(True, alpha=0.3)
        # shade in-market periods
        sig = res["signal"].reindex(res.index).fillna(0)
        ax.fill_between(res.index, 0, res["equity_strat"].max() * 1.05,
                        where=sig > 0, alpha=0.07, color="green", label="_nolegend_")

    # Hide unused subplots
    for j in range(idx + 1, len(axes)):
        axes[j].set_visible(False)

    plt.tight_layout()
    path = os.path.join(out_dir, "equity_curves.png")
    plt.savefig(path, dpi=120, bbox_inches="tight")
    plt.close()
    print(f"[Chart] Saved: {path}")


def plot_summary_bar(summary_df: pd.DataFrame, strategy_name: str, out_dir: str):
    """Bar chart comparing Strategy vs B&H CAGR."""
    fig, axes = plt.subplots(1, 2, figsize=(14, 5))
    fig.suptitle(f"Strategy Summary - {strategy_name}", fontsize=13, fontweight="bold")

    x  = np.arange(len(summary_df))
    w  = 0.35
    tk = summary_df["Ticker"].tolist()

    # CAGR
    ax = axes[0]
    ax.bar(x - w/2, summary_df["Strategy CAGR (%)"], w, label="Strategy", color="#1f77b4")
    ax.bar(x + w/2, summary_df["B&H CAGR (%)"],      w, label="Buy & Hold", color="#ff7f0e")
    ax.set_title("CAGR (%)")
    ax.set_xticks(x); ax.set_xticklabels(tk, rotation=45, ha="right", fontsize=8)
    ax.axhline(0, color="black", lw=0.5)
    ax.legend(); ax.grid(axis="y", alpha=0.3)

    # Max Drawdown
    ax = axes[1]
    ax.bar(x - w/2, summary_df["Strategy MaxDD (%)"], w, label="Strategy", color="#1f77b4")
    ax.bar(x + w/2, summary_df["B&H MaxDD (%)"],      w, label="Buy & Hold", color="#ff7f0e")
    ax.set_title("Max Drawdown (%) - lower is better")
    ax.set_xticks(x); ax.set_xticklabels(tk, rotation=45, ha="right", fontsize=8)
    ax.legend(); ax.grid(axis="y", alpha=0.3)

    plt.tight_layout()
    path = os.path.join(out_dir, "summary_bars.png")
    plt.savefig(path, dpi=120, bbox_inches="tight")
    plt.close()
    print(f"[Chart] Saved: {path}")


def plot_aggregate_equity(results: dict[str, pd.DataFrame], strategy_name: str, out_dir: str):
    """Equal-weight portfolio equity curve."""
    equities_strat = pd.DataFrame({t: r["equity_strat"] for t, r in results.items()})
    equities_bah   = pd.DataFrame({t: r["equity_bah"]   for t, r in results.items()})

    port_strat = equities_strat.mean(axis=1)
    port_bah   = equities_bah.mean(axis=1)

    fig, ax = plt.subplots(figsize=(12, 5))
    ax.plot(port_strat.index, port_strat, color="#1f77b4", lw=2, label="Equal-Weight Portfolio - Strategy")
    ax.plot(port_bah.index,   port_bah,   color="#ff7f0e", lw=1.5, ls="--", label="Equal-Weight Portfolio - Buy & Hold")
    ax.set_title(f"Equal-Weight Portfolio Equity - {strategy_name}", fontsize=13, fontweight="bold")
    ax.set_ylabel("Portfolio Value (Rs1 = base)")
    ax.legend(); ax.grid(True, alpha=0.3)
    plt.tight_layout()
    path = os.path.join(out_dir, "portfolio_equity.png")
    plt.savefig(path, dpi=120, bbox_inches="tight")
    plt.close()
    print(f"[Chart] Saved: {path}")


