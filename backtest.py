"""
Nifty 500 Backtesting Framework
================================
Downloads 10 years of daily OHLCV data for selected Nifty 500 stocks
and runs a configurable strategy. All results are saved to CSV + PNG charts.

Strategies available:
  1. SMA Crossover  – Buy when 50-day SMA crosses above 200-day SMA; sell on cross below.
  2. RSI Mean-Revert – Buy when RSI(14) < 30; sell when RSI > 70.
  3. Momentum        – Buy top-N% by 12-month return each month; rebalance monthly.

Usage:
    python backtest.py                         # uses default settings
    python backtest.py --strategy sma          # SMA Crossover
    python backtest.py --strategy rsi          # RSI Mean Reversion
    python backtest.py --strategy momentum     # Momentum
    python backtest.py --tickers TCS INFY HDFCBANK --strategy sma
"""

