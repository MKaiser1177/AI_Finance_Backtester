#  PERFORMANCE ENGINE
# ═══════════════════════════════════════════════════════

def compute_returns(df: pd.DataFrame, signal: pd.Series) -> pd.DataFrame:
    """Daily strategy returns vs buy-and-hold."""
    close = df["Close"]
    daily_ret   = close.pct_change().fillna(0)
    strat_ret   = daily_ret * signal
    bah_ret     = daily_ret

    equity_strat = (1 + strat_ret).cumprod()
    equity_bah   = (1 + bah_ret).cumprod()

    return pd.DataFrame({
        "daily_strat":  strat_ret,
        "daily_bah":    bah_ret,
        "equity_strat": equity_strat,
        "equity_bah":   equity_bah,
        "signal":       signal,
    })


def max_drawdown(equity: pd.Series) -> float:
    roll_max = equity.cummax()
    dd = (equity - roll_max) / roll_max
    return dd.min()


def cagr(equity: pd.Series) -> float:
    years = (equity.index[-1] - equity.index[0]).days / 365.25
    if years <= 0:
        return 0.0
    return (equity.iloc[-1] / equity.iloc[0]) ** (1 / years) - 1


def sharpe(returns: pd.Series, rf: float = 0.06) -> float:
    """Annualised Sharpe (risk-free = 6% p.a. as proxy for India)."""
    excess = returns - rf / 252
    if excess.std() == 0:
        return 0.0
    return np.sqrt(252) * excess.mean() / excess.std()


def summarise(res: pd.DataFrame, ticker: str) -> dict:
    strat = res["equity_strat"]
    bah   = res["equity_bah"]
    exposure = res["signal"].mean()
    trades   = int(res["signal"].diff().abs().sum() / 2)
    return {
        "Ticker":              ticker,
        "Strategy CAGR (%)":   round(cagr(strat)      * 100, 2),
        "B&H CAGR (%)":        round(cagr(bah)         * 100, 2),
        "Strategy Sharpe":     round(sharpe(res["daily_strat"]), 2),
        "B&H Sharpe":          round(sharpe(res["daily_bah"]),   2),
        "Strategy MaxDD (%)":  round(max_drawdown(strat) * 100, 2),
        "B&H MaxDD (%)":       round(max_drawdown(bah)    * 100, 2),
        "Strategy Final (Rs)":  round(strat.iloc[-1], 4),
        "B&H Final (Rs)":       round(bah.iloc[-1],   4),
        "Market Exposure (%)": round(exposure * 100, 1),
        "Trades":              trades,
    }


