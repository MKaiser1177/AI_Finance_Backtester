def run_backtest(tickers: list[str], strategy: str, out_dir: str, data_dir: str = "data"):
    os.makedirs(out_dir, exist_ok=True)

    # 1. Download data
    data = download_data(tickers, START_DATE, END_DATE, data_dir=data_dir)
    if not data:
        print("[Error] No data downloaded. Exiting.")
        return

    # 1b. Consolidate closing prices (Requirement: 500 companies at once)
    closes_df = pd.DataFrame({tkr: df["Close"] for tkr, df in data.items()})
    consolidated_path = os.path.join(out_dir, "nifty500_all_closes.csv")
    closes_df.to_csv(consolidated_path)
    print(f"[Data] Consolidated daily closing prices (10 yrs) saved to: {consolidated_path}")

    # 2. Pre-compute momentum signals (needs all tickers)
    momentum_signals = {}
    if strategy == "momentum":
        print("[Strategy] Computing momentum signals (this may take a moment) ...")
        momentum_signals = strategy_momentum(data)

    # 3. Run strategy per ticker
    results  = {}
    summaries = []
    STRATEGY_LABEL = {
        "sma":      "SMA Crossover (50/200)",
        "rsi":      "RSI Mean Reversion (14, 30/70)",
        "momentum": "12-Month Momentum (Top 25%)",
    }
    label = STRATEGY_LABEL.get(strategy, strategy)

    print(f"[Backtest] Running '{label}' on {len(data)} stocks ...\n")

    for ticker, df in data.items():
        if strategy == "sma":
            signal = strategy_sma_crossover(df)
        elif strategy == "rsi":
            signal = strategy_rsi_mean_revert(df)
        elif strategy == "momentum":
            signal = momentum_signals.get(ticker, pd.Series(0, index=df.index))
        else:
            raise ValueError(f"Unknown strategy: {strategy}")

        res = compute_returns(df, signal)
        results[ticker]  = res
        summaries.append(summarise(res, ticker))

    # 4. Save summary CSV
    summary_df = pd.DataFrame(summaries).sort_values("Strategy CAGR (%)", ascending=False)
    csv_path   = os.path.join(out_dir, "backtest_summary.csv")
    summary_df.to_csv(csv_path, index=False)
    print(f"\n[Results] Summary saved to: {csv_path}")

    # 5. Print table
    print("\n" + "=" * 90)
    print(f"  BACKTEST RESULTS - {label}")
    print("=" * 90)
    print(summary_df.to_string(index=False))
    print("=" * 90)

    # Portfolio aggregate
    port_strat = pd.DataFrame({t: r["equity_strat"] for t, r in results.items()}).mean(axis=1)
    port_bah   = pd.DataFrame({t: r["equity_bah"]   for t, r in results.items()}).mean(axis=1)
    print(f"\n  EQUAL-WEIGHT PORTFOLIO")
    print(f"  Strategy CAGR : {cagr(port_strat)*100:.2f}%   |   "
          f"B&H CAGR : {cagr(port_bah)*100:.2f}%")
    print(f"  Strategy MaxDD: {max_drawdown(port_strat)*100:.2f}%   |   "
          f"B&H MaxDD: {max_drawdown(port_bah)*100:.2f}%")
    print(f"  Strategy Sharpe: {sharpe(port_strat.pct_change().dropna()):.2f}   |   "
          f"B&H Sharpe: {sharpe(port_bah.pct_change().dropna()):.2f}")
    print("=" * 90 + "\n")

    # 6. Charts
    plot_equity(results, label, out_dir)
    plot_summary_bar(summary_df, label, out_dir)
    plot_aggregate_equity(results, label, out_dir)

    # 7. Save individual equity CSVs
    for ticker, res in results.items():
        res.to_csv(os.path.join(out_dir, f"{ticker}_equity.csv"))

    print(f"\n[Done] All outputs in: ./{out_dir}/")


