def strategy_sma_crossover(df: pd.DataFrame,
                            fast: int = 50,
                            slow: int = 200) -> pd.Series:
    """
    Signal = +1 when 50-SMA > 200-SMA, 0 otherwise (long-only).
    Entry/exit on next day's open after signal fires.
    """
    price  = df["Close"]
    fast_s = sma(price, fast)
    slow_s = sma(price, slow)
    signal = (fast_s > slow_s).astype(int)
    return signal.shift(1).fillna(0)          # execute next day


def strategy_rsi_mean_revert(df: pd.DataFrame,
                              period: int = 14,
                              oversold: float = 30,
                              overbought: float = 70) -> pd.Series:
    """
    Buy when RSI drops below `oversold`; sell when RSI rises above `overbought`.
    """
    r      = rsi(df["Close"], period)
    pos    = pd.Series(0, index=df.index)
    in_pos = False
    for i in range(len(r)):
        if pd.isna(r.iloc[i]):
            pos.iloc[i] = 0
            continue
        if not in_pos and r.iloc[i] < oversold:
            in_pos = True
        elif in_pos and r.iloc[i] > overbought:
            in_pos = False
        pos.iloc[i] = 1 if in_pos else 0
    return pos.shift(1).fillna(0)


def strategy_momentum(data: dict[str, pd.DataFrame],
                       lookback: int = 252,
                       top_pct: float = 0.25) -> dict[str, pd.Series]:
    """
    Monthly rebalance: hold the top `top_pct` of stocks by trailing `lookback`-day return.
    Returns a dict of {ticker: signal_series}.
    """
    closes = pd.DataFrame({t: d["Close"] for t, d in data.items()}).dropna(how="all")
    monthly_dates = closes.resample("ME").last().index

    signals = {t: pd.Series(0.0, index=closes.index) for t in closes.columns}

    for i in range(1, len(monthly_dates)):
        rebal_date = monthly_dates[i]
        # Use index position for safe slicing
        rebal_pos  = closes.index.get_indexer([rebal_date], method="ffill")[0]
        look_pos   = max(0, rebal_pos - lookback)

        window = closes.iloc[look_pos : rebal_pos + 1]
        if len(window) < 20:
            continue
        ret  = window.iloc[-1] / window.iloc[0] - 1
        ret  = ret.dropna()
        n    = max(1, int(len(ret) * top_pct))
        top  = ret.nlargest(n).index.tolist()

        # Hold from this rebalance to next
        next_pos = (closes.index.get_indexer([monthly_dates[i + 1]], method="ffill")[0]
                    if i + 1 < len(monthly_dates) else len(closes))

        for t in top:
            if t in signals:
                signals[t].iloc[rebal_pos : next_pos] = 1.0

    return {t: s.shift(1).fillna(0) for t, s in signals.items()}


