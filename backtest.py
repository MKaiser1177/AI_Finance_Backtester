def download_data(tickers: List[str], start: str, end: str,
                  data_dir: str = "data") -> Dict[str, pd.DataFrame]:
    """
    Load OHLCV data from local CSV files (generated by generate_data.py).
    Falls back to yfinance if a CSV is missing and yfinance is available.
    """
    data   = {}
    failed = []
    to_download = []
    logging.info(f"Checking local files and preparing download for {len(tickers)} tickers...")

    for tkr in tickers:
        csv_path = os.path.join(data_dir, f"{tkr}.csv")
        if os.path.exists(csv_path):
            try:
                df = pd.read_csv(csv_path, index_col="Date", parse_dates=True)
                df = df.loc[start:end]
                if len(df) >= 200:
                    data[tkr] = df[["Open", "High", "Low", "Close", "Volume"]].copy()
                    continue
            except Exception:
                pass
        to_download.append(tkr)

    if to_download:
        print(f"  [yfinance] Downloading {len(to_download)} missing tickers...")
        try:
            import yfinance as yf
            yf_symbols = [t if t.endswith(".NS") else f"{t}.NS" for t in to_download]
            # Grouping by ticker makes extraction more reliable for large sets
            bulk_df = yf.download(yf_symbols, start=start, end=end, 
                                 auto_adjust=True, progress=True, group_by='ticker')
            
            for tkr in to_download:
                yf_sym = tkr if tkr.endswith(".NS") else f"{tkr}.NS"
                try:
                    # Extract individual ticker data from bulk result
                    if len(yf_symbols) > 1:
                        ticker_df = bulk_df[yf_sym]
                    else:
                        ticker_df = bulk_df
                    
                    ticker_df = ticker_df.dropna(subset=["Close"])
                    if not ticker_df.empty:
                        data[tkr] = ticker_df[["Open", "High", "Low", "Close", "Volume"]].copy()
                        # Save to CSV for future local access
                        os.makedirs(data_dir, exist_ok=True)
                        data[tkr].to_csv(os.path.join(data_dir, f"{tkr}.csv"))
                    else:
                        failed.append(tkr)
                except Exception:
                    failed.append(tkr)
        except Exception as e:
            print(f"  X Bulk download failed: {e}")
            failed.extend(to_download)

    if failed:
        print(f"\n[Warning] Could not load: {failed}")
    print(f"[Data] Loaded {len(data)} tickers.\n")
    return data


def get_nifty_500_tickers() -> list[str]:
    """Fetch the latest Nifty 500 constituent list from NSE."""
    url = "https://archives.nseindia.com/content/indices/ind_nifty500list.csv"
    print(f"\n[Data] Fetching latest Nifty 500 list from NSE...")
    try:
        req = urllib.request.Request(url, headers={'User-Agent': 'Mozilla/5.0'})
        with urllib.request.urlopen(req) as response:
            df = pd.read_csv(response)
            tickers = df['Symbol'].tolist()
            print(f"  OK  Fetched {len(tickers)} tickers.")
            return tickers
    except Exception as e:
        print(f"  X  Failed to fetch Nifty 500 list: {e}. Falling back to default list.")
        return DEFAULT_TICKERS_NSE


